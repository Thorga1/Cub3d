#include "cub3d.h"

void	pad_map_with_walls(char **map)
{
	int	i;
	int	j;

	i = 0;
	while (map[i])
	{
		j = 0;
		while (map[i][j])
		{
			if (map[i][j] == ' ')
			map[i][j] = '1';
			j++;
		}
		i++;
	}
}
